/**
 * CMIPS Permission Model - Keycloak Resource-Scope Authorization
 *
 * Uses Keycloak's fine-grained authorization with resources and scopes.
 * Resources represent business modules, scopes represent actions.
 *
 * In Keycloak:
 * - Resources: case-management, timesheet, provider-management, etc.
 * - Scopes: view, create, modify, delete, approve, process, validate, supervise
 * - Policies: Role-based policies mapping roles to access decisions
 * - Permissions: Link resources + scopes + policies
 *
 * The same dashboard is shown to all users - actions are controlled by
 * the user's resource:scope permissions from Keycloak.
 *
 * Core Business Roles Only (51 roles, 15 categories)
 */

