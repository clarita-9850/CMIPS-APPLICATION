/**
 * Get all resource:scope permissions for a user based on their roles.
 * Combines permissions from all assigned roles.
 *
 * @param {string[]} userRoles - Array of Keycloak role names
 * @returns {Object} Map of resource -> Set of scopes
 */
export function getUserPermissions(userRoles) {
  if (!userRoles || !Array.isArray(userRoles)) {
    return {};
  }

  const permissions = {};

  userRoles.forEach(role => {
    const rolePerms = ROLE_PERMISSIONS[role];
    if (rolePerms) {
      Object.entries(rolePerms).forEach(([resource, scopes]) => {
        if (!permissions[resource]) {
          permissions[resource] = new Set();
        }
        scopes.forEach(scope => permissions[resource].add(scope));
      });
    }
  });

  // Convert Sets to Arrays for easier consumption
  const result = {};
  Object.entries(permissions).forEach(([resource, scopes]) => {
    result[resource] = Array.from(scopes);
  });

  return result;
}

/**
 * Check if user can perform a specific action on a resource.
 *
 * @param {string} resource - Resource name (e.g., 'case-management')
 * @param {string} scope - Scope/action name (e.g., 'view', 'modify')
 * @param {string[]} userRoles - Array of Keycloak role names
 * @returns {boolean} True if allowed
 */
export function canAccess(resource, scope, userRoles) {
  if (!resource || !scope || !userRoles) return false;

  const permissions = getUserPermissions(userRoles);
  const resourcePerms = permissions[resource];
  if (!resourcePerms) return false;

  return resourcePerms.includes(scope);
}

/**
 * Check if user can view a resource.
 * @param {string} resource - Resource name
 * @param {string[]} userRoles - Array of Keycloak role names
 * @returns {boolean}
 */
export function canView(resource, userRoles) {
  return canAccess(resource, SCOPES.VIEW, userRoles);
}

/**
 * Check if user can create on a resource.
 * @param {string} resource - Resource name
 * @param {string[]} userRoles - Array of Keycloak role names
 * @returns {boolean}
 */
export function canCreate(resource, userRoles) {
  return canAccess(resource, SCOPES.CREATE, userRoles);
}

